<h2>Business</h2>

<ol>
<li>Reputation reputation reputation.</li>
<li><b>Brooks' Law</b>: Adding manpower to a late project makes it later.</li>
<li><b>Hofstadter's Law</b>: It always takes longer than you expect, even when accounting for Hofstadter's Law.</li>
<li>Try many things. Most will fail, some will work. Learn from the failures.</li>
<li>A smaller and faster deliverable is a better deliverable.</li>
<li>Customers are connections. Create a long-term relationship with them.</li>
<li>Communicate a clear message of intent, strategy, values, and identity to anybody doing work for your company.</li>
<li>In hiring, modesty, communication skills, cultural match, and work ethic are more important than intelligence, skill, and experience.</li>
<li>Only hire people smarter than you.</li>
<li>Seek to make profit by increasing the value of your product, rather than by increasing its price.</li>
<li>Employees are more important than product.</li>
<li>Don't reinvent the wheel. Focus on your company's vision and mission. For everything else, find an external solution.</li>
<li>Pass the monkey.</li>
<li>Cheap, Fast, Quality: pick two.</li>
<li>A good system is one that is sophisticated, but conceals its inner complexity.</li>
<li>Creative/divergent thinking work takes longer when incentivized.</li>
<li>Autonomy, Mastery, Purpose - The three motivators of creative people. Hire amazing people and let them do what they want. Be surprised at the results.</li>
<li>You don't have costs, you have risks.</li>
<li>How to mitigate risk:</li>
<ol><li>Small team</li>
<li>Small scope</li>
<li>Reuse, recycle</li></ol>
<li>Growing a business is like growing a tree. You can give it as much water and sunlight as you want but it still happens slowly. Trying to grow it too fast will only kill it.</li>
<li>What technology is used isn't as important as the preferences of the people who have to use them.</li>
<li>The question isn't, "What's hot?" but rather, "What's going to be hot?"</li>
<li>Working smart is often an excuse for not working hard</li>
<li>If something's not getting done, remove it from your list</li>
<li>From <i>Rework</i> (love that book)</li>
<ol>
	<li>Planning is guessing</li>
	<li>Add profit before employees</li>
