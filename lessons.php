<p><em>Never stop</em></p>

<p>Life goes on no matter what I say or do about it, and there will always be someone trying to eat my lunch. Therefore, I can never stop fighting for what is mine and what I believe in. I do not stop because I am tired, or because I am sick, or because I think I have done enough. I always push myself to the next level. I never stop.</p>

<h2>Franklin's</h2>

<ol>
	<li><strong>Temperance.</strong> Eat not to Dulness. Drink not to Elevation. [Don't drink or eat excessively.]</li>
	<li><strong>Silence.</strong> Speak not but what may benefit others or your self. Avoiding trifling Conversation.</li>
	<li><strong>Order.</strong> Let all your Things have their Places. Let each Rart of your Business have its Time. [Organization. Organize your belongings and your schedule.]</li>
	<li><strong>Resolution.</strong> Resolve to perform what you ought. Perform without fail what you resolve.</li>
	<li><strong>Frugality.</strong> Make no Expense but to do good to others or yourself: i.e. Waste nothing. [Franklin was an environmentalist, apparently.]</li>
	<li><strong>Industry.</strong> Lose no Time. Be always employ'd in something useful. Cut of all unnecessary Actions.</li>
	<li><strong>Sincerity.</strong> Use no hurtful Deceit. Think innocently and justly; and if you speak, speak accordingly. [Sincerity means free from pretense, deceit, or hypocracy. Insincere thought creates insincere actions.]</li>
	<li><strong>Justice.</strong> Wrong none, by doing Injuries or omitting the Benefits that are your Duty. [Harm through neglect is as bad as direct.]</li>
	<li><strong>Moderation.</strong> Avoid extremes. Forbear resenting Injuries so much as you think they deserve. [I think he means don't hold grudges.]</li>
	<li><strong>Cleanliness.</strong> Tolerate no Uncleanliness in Body, Clothes or Habitation.</li>
	<li><strong>Tranquility.</strong> Be not disturbed at Trifles, or at Accidents common or unavoidable. [Also: If you can't change it, don't worry about it. Might possibly name this one Serenity.]</li>
	<li><strong>Chastity.</strong> Rarely use Venery but for Health and Offspring; Never to Dulness, Weakness, or the Injury of your own or another's Peace or Reputation. [18th century ideas on chastity perhaps haven't stood the test of time. But the implied disapproval of sexual misconduct is still strong. I might amend it to <strong>Tolerance</strong> or <strong>Inclusion</strong>.]</li>
	<li><strong>Humility.</strong> Imitate Jesus and Socrates. [Humility I get but I think he lost me with his examples. He explains later: <em>I made it a Rule to forbear all direct Contradiction to the Sentiments of others, and all positive Assertion of my own. I even forbid myself, agreeable to the old Laws of our Junto, the Use of every Word or Expression in the LAnguage that imported a fix'd Opinion; such as </em>certainly, undoubtedly,<em> etc. and I adopted instead of them, </em>I conceive, I apprehend, or I imagine<em> a thing to be so or so, or so it appears to me at the present. When another asserted something that I thought an Error, I denied myself the Pleasure of contradicting him abruptly, and of showing immediately some Absurdity in his Proposition...</em>]</li>
</ol>

<h2>Game Development</h2>

<ol>
<li>Playtesting is king.</li>
<li>Fun is:</li>
<ul>
	<li>Challenge</li>
	<li>Choice - (tradeoffs, risk/reward)</li>
	<li>Gratification</li>
	<li>Eye candy</li>
</ul>
<li>Gamers consider graphics and gameplay to be equally important. <em>(Refer to General rule 3.)</em></li>
<li>Elitists are a vocal minority.</li>
<li>Always know how much information to give to the player, and at what rate and in what order to do it. Too much information and the player is overwhelmed. Too little, and the player is confused.</li>
<li><i>Problems</i> have one definite "best" solution, while <i>Choices</i> are subjective and have no best answer.</li>
<li>No feature is easy to implement. Every feature needs consideration on multiple levels. Visuals, sound, usability, QA.</li>
<li>Just as water seeks its own level, gamers always find the fastest route to a kill, no matter the drawbacks.</li>
<li>No multiplayer action game is worth playing if you can't get into the game in less than 20 seconds and be having fun within another 10.</li>
<li>Don't force players to do something. Instead, provide incentives for them to do it, and let them have fun reaping the rewards.</li>
<li>Use the least amount of plot and exposition as is necessary to tell the story.</li>
<li>Dialogue is for character development, not for plot development.</li>
<li>Games need:</li>
<ul>
	<li>Clear goals</li>
	<li>Immediate feedback</li>
	<li>Appropriate challenge (matched to the user)</li>
	<li>Rewards</li>
</ul>
<li>Give the player a greater purpose.</li>
</ol>

