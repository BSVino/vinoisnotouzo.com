	<li>Know how to tell when people lie, when they will break promises, and how they will hurt you.</li>
	<li>Remember Murphy's Law.</li>
	<li>Never hold anybody else to your standards.</li>
	<li>Don't waste time worrying about things that you can't do anything about.</li>
	<li>Stay healthy. Exercise. Push your physical limits. A healthy body leads to a healthy mind.</li>
	<li>Stay smart. Exercise your brain. Engage in intellectualisms. Challenge your mind. A healthy mind leads to a healthy spirit.</li>
	<li>Stay balanced. Meditate. Re-evaluate your life. Reflect on your situation. All is lost without a healthy spirit.</li>
	<li>Remember Sammy Jankis.</li>
	<li>Someone is always trying to eat your lunch. You have to be better than them or you will starve.</li>
	<li>Listen when people criticize you. Don't defend yourself. Fix yourself.</li>
